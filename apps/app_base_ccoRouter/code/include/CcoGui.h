/*******************************************************************************
* 本地通信app 显示GUI相关接口
*******************************************************************************/

/*******************************************************************************
* 函数名称: gui_hplc_fr_query
* 函数功能: 显示GUI 查看当前HPLC频点
* 输入参数: uint8 ModuleID 本地模块序号(1或者2)
* 输出参数: uint8 *Fr 频点值
* 返 回 值: 大于0：正确；小于0：错误
*******************************************************************************/
extern int gui_hplc_fr_query(uint8 ModuleID, uint8 *Fr);


/*******************************************************************************
* 函数名称: gui_amr_port_query
* 函数功能: 显示GUI 查看当前抄表端口信息
* 输入参数: uint8 ModuleID 本地模块序号(1或者2)
* 输出参数: uint32 *Port 端口号
* 返 回 值: 大于0：正确；小于0：错误
*******************************************************************************/
extern int gui_amr_port_query(uint8 ModuleID, uint32 *Port);


/*******************************************************************************
* 函数名称: gui_module_addr_query
* 函数功能: 显示GUI 查看当前模块地址
* 输入参数: uint8 ModuleID 本地模块序号(1或者2)
			uint8 Len 地址缓冲区长度(6)
* 输出参数: uint8 *Addr 模块地址
* 返 回 值: 大于0：正确；小于0：错误
*******************************************************************************/
extern int gui_module_addr_query(uint8 ModuleID, uint8 Len, uint8 *Addr);


