/*******************************************************************************
* 函数名称: gui_files_count_query
* 函数功能: 显示GUI 查看档案数量
* 输入参数: uint8 ModuleID 本地模块序号(1或者2)
* 输出参数: 无
* 返 回 值: >=0:档案数量； -1:错误
*******************************************************************************/
extern int gui_files_count_query(uint8 ModuleID);


